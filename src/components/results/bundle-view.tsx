"use client";

import { useEffect, useMemo, useState } from "react";
import { motion } from "framer-motion";
import {
  Check,
  Copy,
  Download,
  FileText,
  Loader2,
  Share2,
  Zap,
} from "lucide-react";
import { strToU8, zipSync } from "fflate";
import { ScoreModal } from "@/components/results/score-modal";
import type {
  AnalysisCompleteEvent,
  BundleFileEvent,
  ScoreEvent,
} from "@/lib/analyze/events";

/** Results Phase 2 — two-pane bundle browser per 01 §20. */

function relativeTime(iso: string): string {
  const diffMs = Date.now() - new Date(iso).getTime();
  const minutes = Math.round(diffMs / 60000);
  if (minutes < 2) return "just now";
  if (minutes < 60) return `${minutes}m ago`;
  const hours = Math.round(minutes / 60);
  return hours < 48 ? `${hours}h ago` : `${Math.round(hours / 24)}d ago`;
}

function provenanceLine(file: BundleFileEvent): string {
  const p = file.provenance;
  if (file.status === "skipped") {
    return `skipped · ${file.skipReason ?? "unknown"} · bundle ships without it`;
  }
  if (file.origin === "official" && p?.sources[0]) {
    const domain = new URL(p.sources[0].url).hostname;
    return `source: official · ${domain} · fetched ${relativeTime(p.fetchedAt)}`;
  }
  if (file.origin === "generated" || file.origin === "generated-cached") {
    const grounding = p?.sources.map((s) => new URL(s.url).hostname).join(" + ");
    const verified =
      p?.verifiedCommands !== undefined
        ? ` · ${p.verifiedCommands} commands verified`
        : "";
    const stripped =
      p?.strippedClaims !== undefined
        ? ` · ${p.strippedClaims} unverifiable claims removed`
        : "";
    return `generated${grounding ? ` · grounded in ${grounding}` : ""}${verified}${stripped}`;
  }
  const verified =
    p?.verifiedCommands !== undefined
      ? ` · ${p.verifiedCommands} commands verified`
      : "";
  const stripped = p?.strippedClaims ? ` · ${p.strippedClaims} unverifiable claims removed` : "";
  return `deterministic · parsed from repo facts${verified}${stripped}`;
}

function statusGlyph(file: BundleFileEvent) {
  if (file.status === "skipped") {
    return (
      <span className="text-warning" title={file.skipReason}>
        !
      </span>
    );
  }
  if (file.origin === "official") {
    return <Check className="size-3.5 text-success" strokeWidth={2} aria-label="official" />;
  }
  return <Zap className="size-3.5 text-primary" strokeWidth={2} aria-label="generated" />;
}

export function BundleView({
  owner,
  repo,
  files,
  score,
  complete,
}: {
  owner: string;
  repo: string;
  files: BundleFileEvent[];
  score: ScoreEvent | null;
  complete: AnalysisCompleteEvent | null;
}) {
  const sorted = useMemo(
    () => [...files].sort((a, b) => a.sortOrder - b.sortOrder || a.path.localeCompare(b.path)),
    [files],
  );
  const [selected, setSelected] = useState<string | null>(null);
  const [copied, setCopied] = useState<string | null>(null);
  const active =
    sorted.find((f) => f.path === selected) ??
    sorted.find((f) => f.status === "complete") ??
    null;

  useEffect(() => {
    if (!selected && sorted.length > 0) {
      const first = sorted.find((f) => f.status === "complete");
      if (first) setSelected(first.path);
    }
  }, [sorted, selected]);

  const flashCopied = (key: string) => {
    setCopied(key);
    setTimeout(() => setCopied(null), 1200);
  };

  const downloadZip = () => {
    const entries: Record<string, Uint8Array> = {};
    for (const file of sorted) {
      if (file.status === "complete" && file.content) {
        entries[file.path] = strToU8(file.content);
      }
    }
    const zipped = zipSync(entries);
    const arrayBuffer = new ArrayBuffer(zipped.byteLength);
    new Uint8Array(arrayBuffer).set(zipped);
    const blob = new Blob([arrayBuffer], { type: "application/zip" });
    const url = URL.createObjectURL(blob);
    const anchor = document.createElement("a");
    anchor.href = url;
    anchor.download = `gitbrief-${owner}-${repo}.zip`;
    anchor.click();
    URL.revokeObjectURL(url);
  };

  const copyAllAsPrompt = async () => {
    const parts = sorted
      .filter((f) => f.status === "complete" && f.content)
      .map((f) => `===== ${f.path} =====\n\n${f.content}`);
    const text = parts.join("\n\n");
    const tokens = Math.ceil(text.length / 4);
    await navigator.clipboard.writeText(
      `${text}\n\n---\n~${tokens.toLocaleString()} tokens · generated by gitbrief for ${owner}/${repo}`,
    );
    flashCopied("prompt");
  };

  const shareUrl = async () => {
    await navigator.clipboard.writeText(window.location.href);
    flashCopied("share");
  };

  const copyFile = async () => {
    if (active?.content) {
      await navigator.clipboard.writeText(active.content);
      flashCopied("file");
    }
  };

  return (
    <motion.div
      initial={{ opacity: 0, y: 16 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.3 }}
      className="mx-auto mt-8 w-full max-w-[1100px] px-6"
    >
      {/* Action bar — sticky (01 §20) */}
      <div className="sticky top-16 z-40 flex flex-wrap items-center gap-3 rounded-[12px] border border-border bg-background/90 px-4 py-3 backdrop-blur-md">
        {score && <ScoreModal score={score} />}
        <span className="flex-1" />
        <button
          type="button"
          onClick={downloadZip}
          disabled={!complete}
          className="flex h-9 items-center gap-2 rounded-[12px] bg-primary px-4 text-sm font-medium text-white transition-colors hover:bg-primary-hover disabled:opacity-60"
        >
          {complete ? (
            <Download className="size-4" strokeWidth={1.5} />
          ) : (
            <Loader2 className="size-4 animate-spin" strokeWidth={1.5} />
          )}
          Download .zip
        </button>
        <button
          type="button"
          onClick={() => void copyAllAsPrompt()}
          disabled={!complete}
          className="flex h-9 items-center gap-2 rounded-[12px] border border-primary px-4 text-sm font-medium text-primary transition-colors hover:bg-primary-soft disabled:opacity-60"
        >
          {copied === "prompt" ? (
            <Check className="size-4" strokeWidth={1.5} />
          ) : (
            <Copy className="size-4" strokeWidth={1.5} />
          )}
          Copy all as one prompt
        </button>
        <button
          type="button"
          onClick={() => void shareUrl()}
          aria-label="Copy link"
          className="flex size-9 items-center justify-center rounded-[12px] border border-border text-muted-foreground transition-colors hover:text-foreground"
        >
          {copied === "share" ? (
            <Check className="size-4 text-success" strokeWidth={1.5} />
          ) : (
            <Share2 className="size-4" strokeWidth={1.5} />
          )}
        </button>
      </div>

      <div className="mt-4 grid gap-4 md:grid-cols-[280px_1fr]">
        {/* Sidebar tree */}
        <nav aria-label="Bundle files" className="max-md:overflow-x-auto">
          <ul className="flex gap-1 md:flex-col">
            {sorted.map((file) => (
              <li key={file.path}>
                <button
                  type="button"
                  onClick={() => setSelected(file.path)}
                  disabled={file.status === "skipped"}
                  className={`flex w-full items-center gap-2 rounded-[8px] px-3 py-2 text-left font-mono text-[0.8125rem] transition-colors max-md:whitespace-nowrap ${
                    active?.path === file.path
                      ? "bg-primary-soft text-foreground"
                      : "text-muted-foreground hover:text-foreground"
                  } ${file.status === "skipped" ? "opacity-60" : ""}`}
                >
                  <FileText className="size-3.5 shrink-0" strokeWidth={1.5} aria-hidden="true" />
                  <span className="truncate">{file.path}</span>
                  <span className="ml-auto shrink-0">{statusGlyph(file)}</span>
                </button>
              </li>
            ))}
          </ul>
        </nav>

        {/* Viewer */}
        <div className="overflow-hidden rounded-[16px] bg-code-bg">
          {active?.content ? (
            <>
              <div className="flex items-center justify-between border-b border-white/10 px-4 py-2">
                <span className="font-mono text-xs text-white/70">{active.path}</span>
                <button
                  type="button"
                  onClick={() => void copyFile()}
                  aria-label={`Copy ${active.path}`}
                  className="rounded-[8px] border border-white/15 p-1.5 text-white/70 transition-colors hover:text-white"
                >
                  {copied === "file" ? (
                    <Check className="size-3.5 text-success" strokeWidth={1.5} />
                  ) : (
                    <Copy className="size-3.5" strokeWidth={1.5} />
                  )}
                </button>
              </div>
              <pre className="max-h-[560px] overflow-auto p-4 font-mono text-[0.8125rem] leading-6 text-[#EFEDF6] whitespace-pre-wrap">
                {active.content}
              </pre>
              <p className="border-t border-white/10 px-4 py-2 font-mono text-xs text-white/50">
                {provenanceLine(active)}
              </p>
              <span aria-live="polite" className="sr-only">
                {copied ? "Copied" : ""}
              </span>
            </>
          ) : (
            <div className="space-y-2 p-4" aria-hidden="true">
              {Array.from({ length: 8 }).map((_, i) => (
                <div
                  key={i}
                  className="h-4 animate-pulse rounded bg-white/10"
                  style={{ width: `${90 - i * 8}%` }}
                />
              ))}
            </div>
          )}
        </div>
      </div>

      {complete && (
        <p className="mt-4 text-center font-mono text-xs text-muted-foreground">
          {complete.fileCount} files
          {complete.skippedCount > 0 ? ` · ${complete.skippedCount} skipped` : ""} ·{" "}
          {complete.verifiedCommands} commands verified · {complete.strippedClaims}{" "}
          unverifiable claims removed
        </p>
      )}
    </motion.div>
  );
}
